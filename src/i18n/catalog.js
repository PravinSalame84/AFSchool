const catalogs = { hi: { ...hi, ...hiData }, mr: { ...mr, ...mrData } }

export function translate(locale, value) {
  if (!value || locale === 'en') return value
  return catalogs[locale]?.[value] ?? value
}

export function localizeValue(locale, value) {
  if (typeof value === 'string') return translate(locale, value)
  if (Array.isArray(value)) return value.map((item) => localizeValue(locale, item))
  if (value && typeof value === 'object') {
    return Object.fromEntries(Object.entries(value).map(([key, entry]) => [key, localizeValue(locale, entry)]))
  }
  return value
}
